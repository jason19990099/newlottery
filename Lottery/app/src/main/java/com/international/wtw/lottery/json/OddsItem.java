public class OddsItem {
    /**
     * name : 大
     * odds : 1.988
     * key : ip_3001_3011
     */
    public String name;
    public String odds;
    public String key;

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getOdds() {
        return this.odds;
    }
    public void setOdds(String odds) {
        this.odds = odds;
    }
    public String getKey() {
        return this.key;
    }
    public void setKey(String key) {
        this.key = key;
    }
}
